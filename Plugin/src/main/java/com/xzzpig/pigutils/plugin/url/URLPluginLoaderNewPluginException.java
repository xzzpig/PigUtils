package com.xzzpig.pigutils.plugin.url;

public class URLPluginLoaderNewPluginException extends RuntimeException {

	private static final long serialVersionUID = -2789112790905545908L;

	public URLPluginLoaderNewPluginException() {
	}

	public URLPluginLoaderNewPluginException(String message) {
		super(message);
	}

	public URLPluginLoaderNewPluginException(Throwable cause) {
		super(cause);
	}

	public URLPluginLoaderNewPluginException(String message, Throwable cause) {
		super(message, cause);
	}

	public URLPluginLoaderNewPluginException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
